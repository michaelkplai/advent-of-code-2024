function solve1(input: string): number {
  let count = 0;

  const g = input.split("\n").map((line) => line.split(""));
  const xLength = g[0].length;
  const yLength = g.length;

  for (let x = 0; x < xLength; x++) {
    for (let y = 0; y < yLength; y++) {
      if (g[y][x] !== "X") continue;

      // Right
      if (
        x + 3 < xLength &&
        g[y][x] + g[y][x + 1] + g[y][x + 2] + g[y][x + 3] === "XMAS"
      ) {
        count++;
      }
      // Left
      if (
        x - 3 >= 0 &&
        g[y][x] + g[y][x - 1] + g[y][x - 2] + g[y][x - 3] === "XMAS"
      ) {
        count++;
      }

      // Downwards
      if (
        y + 3 < yLength &&
        g[y][x] + g[y + 1][x] + g[y + 2][x] + g[y + 3][x] === "XMAS"
      ) {
        count++;
      }
      // Upwards
      if (
        y - 3 >= 0 &&
        g[y][x] + g[y - 1][x] + g[y - 2][x] + g[y - 3][x] === "XMAS"
      ) {
        count++;
      }

      // south-east
      if (
        x + 3 < xLength &&
        y + 3 < yLength &&
        g[y][x] + g[y + 1][x + 1] + g[y + 2][x + 2] + g[y + 3][x + 3] === "XMAS"
      ) {
        count++;
      }
      // north-east
      if (
        x + 3 < xLength &&
        y - 3 >= 0 &&
        g[y][x] + g[y - 1][x + 1] + g[y - 2][x + 2] + g[y - 3][x + 3] === "XMAS"
      ) {
        count++;
      }
      // north-west
      if (
        x - 3 >= 0 &&
        y - 3 >= 0 &&
        g[y][x] + g[y - 1][x - 1] + g[y - 2][x - 2] + g[y - 3][x - 3] === "XMAS"
      ) {
        count++;
      }
      // south-west
      if (
        x - 3 >= 0 &&
        y + 3 < yLength &&
        g[y][x] + g[y + 1][x - 1] + g[y + 2][x - 2] + g[y + 3][x - 3] === "XMAS"
      ) {
        count++;
      }
    }
  }

  return count;
}

