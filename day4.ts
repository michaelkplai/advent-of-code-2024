function solve2(input: string): number {
  let count = 0;

  const g = input.split("\n").map((line) => line.split(""));
  const xLength = g[0].length;
  const yLength = g.length;

  for (let x = 0; x < xLength; x++) {
    for (let y = 0; y < yLength; y++) {
      if (g[y][x] !== "A") continue;
      if (x - 1 < 0 || x + 1 >= xLength || y - 1 < 0 || y + 1 >= yLength)
        continue;

      const diag1Correct =
        (g[y - 1][x - 1] === "M" && g[y + 1][x + 1] === "S") ||
        (g[y - 1][x - 1] === "S" && g[y + 1][x + 1] === "M");
      const diag2Correct =
        (g[y + 1][x - 1] === "M" && g[y - 1][x + 1] === "S") ||
        (g[y + 1][x - 1] === "S" && g[y - 1][x + 1] === "M");

      if (diag1Correct && diag2Correct) count++;
    }
  }

  return count;
}

